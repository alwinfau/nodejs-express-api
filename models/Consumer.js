import {
    DataTypes
} from 'sequelize';
import sequelize from './sequelize.js';

const Consumer = sequelize.define('Consumer', {
    name: {
        type: DataTypes.STRING,
        allowNull: false,
    },
    email: {
        type: DataTypes.STRING,
        allowNull: false,
        unique: true,
    },
    telp: {
        type: DataTypes.STRING,
        allowNull: false,
        unique: true,
    },
    address: {
        type: DataTypes.STRING,
        allowNull: false,
    },
});

export default Consumer;